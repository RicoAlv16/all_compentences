    <script>
      // Gestion du formulaire multi-étapes
      class MultiStepForm {
        constructor() {
          this.currentStep = 1;
          this.totalSteps = 8;
          this.formData = {};
          
          this.init();
        }
        
        init() {
          this.bindEvents();
          this.updateProgress();
        }
        
        bindEvents() {
          const nextBtn = document.getElementById('next-btn');
          const prevBtn = document.getElementById('prev-btn');
          const form = document.getElementById('contact-form');
          
          nextBtn.addEventListener('click', () => this.nextStep());
          prevBtn.addEventListener('click', () => this.prevStep());
          form.addEventListener('submit', (e) => this.handleSubmit(e));
          
          // Validation en temps réel
          const inputs = form.querySelectorAll('input, textarea, select');
          inputs.forEach(input => {
            input.addEventListener('blur', () => this.validateField(input));
            input.addEventListener('input', () => this.clearError(input));
          });
        }
        
        nextStep() {
          if (this.validateCurrentStep()) {
            this.saveCurrentStepData();
            
            if (this.currentStep < this.totalSteps) {
              this.currentStep++;
              this.showStep(this.currentStep);
            } else {
              // Afficher le récapitulatif
              this.showRecap();
            }
            
            this.updateProgress();
            this.updateButtons();
          }
        }
        
        prevStep() {
          if (this.currentStep > 1) {
            this.currentStep--;
            this.showStep(this.currentStep);
            this.updateProgress();
            this.updateButtons();
          }
        }
        
        showStep(stepNumber) {
          const steps = document.querySelectorAll('.form-step');
          
          steps.forEach((step, index) => {
            step.classList.remove('active', 'prev');
            
            if (index + 1 === stepNumber) {
              step.classList.add('active');
              step.style.transform = 'translateX(0)';
              step.style.opacity = '1';
            } else if (index + 1 < stepNumber) {
              step.classList.add('prev');
              step.style.transform = 'translateX(-100%)';
              step.style.opacity = '0';
            } else {
              step.style.transform = 'translateX(100%)';
              step.style.opacity = '0';
            }
          });
          
          // Focus sur le premier input de l'étape
          setTimeout(() => {
            const activeStep = document.querySelector('.form-step.active');
            const firstInput = activeStep.querySelector('input, textarea');
            if (firstInput) {
              firstInput.focus();
            }
          }, 300);
        }
        
        validateCurrentStep() {
          const currentStepElement = document.querySelector(`[data-step="${this.currentStep}"]`);
          const inputs = currentStepElement.querySelectorAll('input[required], textarea[required]');
          let isValid = true;
          
          inputs.forEach(input => {
            if (!this.validateField(input)) {
              isValid = false;
            }
          });
          
          // Validation spéciale pour le type de personne
          if (this.currentStep === 6) {
            const typeInputs = document.querySelectorAll('input[name="type_personne"]');
            const isTypeSelected = Array.from(typeInputs).some(input => input.checked);
            if (!isTypeSelected) {
              this.showError('type_personne', 'Veuillez sélectionner un type');
              isValid = false;
            }
          }
          
          return isValid;
        }
        
        validateField(field) {
          const value = field.value.trim();
          const fieldName = field.name;
          let isValid = true;
          
          // Validation required
          if (field.hasAttribute('required') && !value) {
            this.showError(fieldName, 'Ce champ est obligatoire');
            return false;
          }
          
          // Validation email
          if (field.type === 'email' && value) {
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (!emailRegex.test(value)) {
              this.showError(fieldName, 'Veuillez entrer un email valide');
              return false;
            }
          }
          
          // Validation téléphone
          if (field.type === 'tel' && value) {
            const phoneRegex = /^[0-9\s\-\+\(\)]{10,}$/;
            if (!phoneRegex.test(value)) {
              this.showError(fieldName, 'Veuillez entrer un numéro valide');
              return false;
            }
          }
          
          this.clearError(fieldName);
          return true;
        }
        
        showError(fieldName, message) {
          const errorElement = document.getElementById(`${fieldName}-error`);
          if (errorElement) {
            errorElement.textContent = message;
            errorElement.classList.add('show');
          }
        }
        
        clearError(fieldName) {
          const errorElement = document.getElementById(`${fieldName}-error`);
          if (errorElement) {
            errorElement.classList.remove('show');
          }
        }
        
        saveCurrentStepData() {
          const currentStepElement = document.querySelector(`[data-step="${this.currentStep}"]`);
          const inputs = currentStepElement.querySelectorAll('input, textarea');
          
          inputs.forEach(input => {
            if (input.type === 'radio') {
              if (input.checked) {
                this.formData[input.name] = input.value;
              }
            } else {
              this.formData[input.name] = input.value;
            }
          });
        }
        
        showRecap() {
          this.currentStep = 9;
          this.showStep(9);
          
          // Remplir le récapitulatif
          document.getElementById('recap-nom').textContent = this.formData.nom || '';
          document.getElementById('recap-prenom').textContent = this.formData.prenom || '';
          document.getElementById('recap-email').textContent = this.formData.email || '';
          document.getElementById('recap-telephone').textContent = this.formData.telephone || 'Non renseigné';
          document.getElementById('recap-adresse').textContent = this.formData.adresse || '';
          document.getElementById('recap-type').textContent = this.formData.type_personne === 'freelance' ? 'Freelance' : 'Entreprise';
          document.getElementById('recap-sujet').textContent = this.formData.sujet || '';
          document.getElementById('recap-message').textContent = this.formData.message || '';
        }
        
        updateProgress() {
          const progressBar = document.getElementById('progress-bar');
          const currentStepSpan = document.getElementById('current-step');
          const progressPercent = document.getElementById('progress-percent');
          
          const progress = (this.currentStep / (this.totalSteps + 1)) * 100;
          
          progressBar.style.width = `${progress}%`;
          currentStepSpan.textContent = this.currentStep;
          progressPercent.textContent = Math.round(progress);
        }
        
        updateButtons() {
          const nextBtn = document.getElementById('next-btn');
          const prevBtn = document.getElementById('prev-btn');
          const submitBtn = document.getElementById('submit-btn');
          
          // Bouton précédent
          if (this.currentStep > 1) {
            prevBtn.style.display = 'flex';
          } else {
            prevBtn.style.display = 'none';
          }
          
          // Boutons suivant/envoyer
          if (this.currentStep === 9) {
            nextBtn.style.display = 'none';
            submitBtn.style.display = 'flex';
          } else {
            nextBtn.style.display = 'flex';
            submitBtn.style.display = 'none';
          }
        }
        
        async handleSubmit(e) {
          e.preventDefault();
          
          const submitBtn = document.getElementById('submit-btn');
          submitBtn.classList.add('loading');
          
          try {
            // Simulation d'envoi (remplacez par votre logique d'envoi)
            await this.submitForm();
            
            // Afficher un message de succès
            this.showSuccess();
          } catch (error) {
            console.error('Erreur lors de l\'envoi:', error);
            alert('Une erreur est survenue. Veuillez réessayer.');
          } finally {
            submitBtn.classList.remove('loading');
          }
        }
        
        async submitForm() {
          try {
            const response = await fetch('all-competences/back/logique.php', {
              method: 'POST',
              headers: {
                'Content-Type': 'application/json',
              },
              body: JSON.stringify(this.formData)
            });
            
            const result = await response.json();
            
            if (result.success) {
              return result;
            } else {
              throw new Error(result.message || 'Erreur lors de l\'envoi');
